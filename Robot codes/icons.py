def draw_mini_icon(fb, icon_data, x, y):
    """Blit an 8x8 mini icon."""
    icon_fb = framebuf.FrameBuffer(bytearray(icon_data), 8, 8, framebuf.MONO_HLSB)
    fb.blit(icon_fb, x, y, 0)
