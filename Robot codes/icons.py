def draw_icon(fb, icon_name, x, y, c=1):
    """
    Blit a 16x16 icon onto the framebuffer at position (x, y).
    """
    data = ICONS.get(icon_name)
    if data is None:
        fb.rect(x, y, 16, 16, c)
        return
    icon_fb = framebuf.FrameBuffer(data, 16, 16, framebuf.MONO_HLSB)
    if c:
        fb.blit(icon_fb, x, y, 0)
        return
    for yy in range(16):
        for xx in range(16):
            if icon_fb.pixel(xx, yy):
                fb.pixel(x + xx, y + yy, 0)
